public class AndOrXOR_op {
    public static void main(String args[]){
        //Binary AND
        int a=5&6;             //trick same pr same, different pr 0.
        //Binary OR
        int b=5|6;             //trick same pr same, different pr 1.
        //Binary XOR
        int c=5^6;              //trick same pr 0, different pr 1.
        System.out.println("AND= "+a);
        System.out.println("OR= "+b);
        System.out.println("XOR= "+c);
       } 
}
